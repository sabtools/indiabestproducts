// ── Category Mapping ────────────────────────────────────────

const CATEGORY_MAP: Record<string, ApiProductCategory[]> = {
  phone: ['phone'],
  laptop: ['laptop'],
  headphone: ['headphone'],
  tablet: ['tablet'],
  smartwatch: ['smartwatch'],
  tv: ['tv'],
  camera: ['camera'],
  appliance: ['appliance'],
};

// ── Search / Filter Helpers ─────────────────────────────────

function filterByCategory(products: ProductListing[], category: string): ProductListing[] {
  const cats = CATEGORY_MAP[category];
  if (!cats) return products;
  return products.filter((p) => cats.includes(p.category));
}

function filterByKeyword(products: ProductListing[], keyword: string): ProductListing[] {
  const lower = keyword.toLowerCase();
  return products.filter(
    (p) =>
      p.name.toLowerCase().includes(lower) ||
      p.brand.toLowerCase().includes(lower) ||
      p.keywords.some((k) => k.includes(lower))
  );
}

// ── Amazon Mock Data Accessors ──────────────────────────────

export function getMockAmazonProducts(keyword: string, category: string): ProductListing[] {
  let results = filterByCategory(ALL_MOCK_PRODUCTS, category);
  if (keyword && keyword !== 'best seller') {
    results = filterByKeyword(results, keyword);
  }
  // Return only Amazon price data
  return results.map((p) => ({
    ...p,
    prices: p.prices.filter((pr) => pr.platform === 'amazon'),
    bestPrice: p.prices.find((pr) => pr.platform === 'amazon') || p.bestPrice,
  }));
}

export function getMockAmazonProduct(asin: string): ProductListing | null {
  const product = ALL_MOCK_PRODUCTS.find((p) => p.amazonAsin === asin);
  if (!product) return null;
  return {
    ...product,
    prices: product.prices.filter((pr) => pr.platform === 'amazon'),
    bestPrice: product.prices.find((pr) => pr.platform === 'amazon') || product.bestPrice,
  };
}

