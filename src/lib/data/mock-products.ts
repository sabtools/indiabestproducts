import { ProductListing, PlatformPrice, ApiProductCategory } from '../api/types';

// ── Helper: Build a complete ProductListing ──────────────────

function buildProduct(p: {
  name: string;
  slug: string;
  brand: string;
  category: ApiProductCategory;
  image: string;
  description: string;
  specs: Record<string, string>;
  amazonPrice: number;
  amazonMrp: number;
  flipkartPrice: number;
  flipkartMrp: number;
  amazonAsin: string;
  flipkartPid: string;
  amazonRating: number;
  amazonReviews: number;
  flipkartRating: number;
  flipkartReviews: number;
  keywords: string[];
}): ProductListing {
  const amazonPrice: PlatformPrice = {
    platform: 'amazon',
    price: p.amazonPrice,
    originalPrice: p.amazonMrp,
    discount: Math.round(((p.amazonMrp - p.amazonPrice) / p.amazonMrp) * 100),
    url: `https://www.amazon.in/dp/${p.amazonAsin}?tag=indiabestprod-21`,
    inStock: true,
    rating: p.amazonRating,
    reviewCount: p.amazonReviews,
    seller: 'Amazon.in',
    deliveryInfo: 'FREE delivery by Amazon',
    lastUpdated: '2026-03-26',
  };

  const flipkartPrice: PlatformPrice = {
    platform: 'flipkart',
    price: p.flipkartPrice,
    originalPrice: p.flipkartMrp,
    discount: Math.round(((p.flipkartMrp - p.flipkartPrice) / p.flipkartMrp) * 100),
    url: `https://www.flipkart.com/product/${p.flipkartPid}?affid=indiabestprod`,
    inStock: true,
    rating: p.flipkartRating,
    reviewCount: p.flipkartReviews,
    seller: 'Flipkart',
    deliveryInfo: 'FREE delivery by Flipkart',
    lastUpdated: '2026-03-26',
  };

  const prices = [amazonPrice, flipkartPrice];
  const bestPrice = amazonPrice.price <= flipkartPrice.price ? amazonPrice : flipkartPrice;
  const priceDifference = Math.abs(amazonPrice.price - flipkartPrice.price);

  return {
    name: p.name,
    slug: p.slug,
    brand: p.brand,
    category: p.category,
    image: `/images/products/${p.slug}.webp`,
    description: p.description,
    specs: p.specs,
    prices,
    bestPrice,
    priceDifference,
    amazonAsin: p.amazonAsin,
    flipkartPid: p.flipkartPid,
    keywords: p.keywords,
    metaTitle: `${p.name} — Best Price in India ${new Date().getFullYear()} | Amazon vs Flipkart`,
    metaDescription: `Buy ${p.name} at best price Rs ${Math.min(p.amazonPrice, p.flipkartPrice).toLocaleString('en-IN')}. Compare Amazon & Flipkart prices, specs, reviews.`,
  };
}

